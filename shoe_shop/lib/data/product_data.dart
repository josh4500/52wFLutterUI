  "Asics",
  "Jordan"
];

List<Product> getProducts({String? category, required String brand}) {
  switch (brand) {
    case "Nike":
      return category == null
          ? nikeProducts
          : nikeProducts.where((p) => p.category == category).toList();
    case "Adidas":
      return category == null
          ? adidasProducts
          : adidasProducts.where((p) => p.category == category).toList();
    case "Puma":
      return category == null
          ? pumaProducts
          : pumaProducts.where((p) => p.category == category).toList();
    case "Reebok":
      return category == null
          ? reebokProducts
          : reebokProducts.where((p) => p.category == category).toList();
    case "Asics":
      return category == null
          ? asicsProducts
          : asicsProducts.where((p) => p.category == category).toList();
    case "Jordan":
      return category == null
          ? jordanProducts
          : jordanProducts.where((p) => p.category == category).toList();
    default:
      return category == null
          ? nikeProducts
          : nikeProducts.where((p) => p.category == category).toList();
  }
}
